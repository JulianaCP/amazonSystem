    private void bottonAcceptVarificateAreaOfProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottonAcceptVarificateAreaOfProductActionPerformed
        

                /*inputs: the user selected radio button
        output: send the user to some window (depends in how option the user choose)
        or send an advice to the user.
        restrictions: the user canÂ´t live empty spaces
        functions: the option choose for the user by the radio button send 
        to the user to the difetents windows for create some type of product (Electrodomestic
        Furniture or Clothes)
        */  
        
        if( ! radioButtonClothes.isSelected() && ! radioButtonElectrodomestics.isSelected() && ! radioButtonFurniture.isSelected() ){
            return;
        }
        
        
        if(radioButtonFurniture.isSelected()){
            WindowCreateProductFurnitureInformation window = new WindowCreateProductFurnitureInformation();
            window.setVisible(true);
            this.dispose();
            
        }
        else if(radioButtonElectrodomestics.isSelected()){
            WindowCreateProductElectrodomesticInformation window = new WindowCreateProductElectrodomesticInformation();
            window.setVisible(true);
            this.dispose();
        }
        else if(radioButtonClothes.isSelected()){
            WindowCreateProductClothesInformation window = new WindowCreateProductClothesInformation();
            window.setVisible(true);
            this.dispose();
            
        }
        else{
            JOptionPane.showMessageDialog(this,"You cant live black space ");
        }
           
    }//GEN-LAST:event_bottonAcceptVarificateAreaOfProductActionPerformed

